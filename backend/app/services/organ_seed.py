def _slug(s: str) -> str:
    s = s.lower().strip()
    s = re.sub(r"[ /]+", "_", s)
    s = re.sub(r"[^a-zа-яё0-9_]", "", s)
    return s or "_"


def _iter_canon() -> Iterable[dict]:
    for bank, names in _CANON.items():
        for name in names:
            code = f"{bank}.{_slug(name)}"
            yield {
                "bank": bank,
                "code": code,
                "name": name,
                "description": None,
                "source": "canon_v0.1",
            }


def seed_organs_if_empty() -> dict:
    db: Session = SessionLocal()
    try:
        existing = {o.code for o in db.query(Organ.code).all()}
        added = 0
        for o in _iter_canon():
            if o["code"] in existing:
                continue
            db.add(Organ(**o))
            added += 1
        if added:
            db.commit()
        return {"added": added, "total": db.query(Organ).count()}
    finally:
        db.close()
